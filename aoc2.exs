defmodule AOC2 do
  def compute(commands) do
    Enum.reduce(commands, {0, 0}, fn cmd, {horizontal, depth} ->
      case cmd do
        ["forward", value] -> {horizontal + value, depth}
        ["up", value] -> {horizontal, depth - value}
        ["down", value] -> {horizontal, depth + value}
        _ ->
          IO.puts("unknown command")
          {horizontal, depth}
      end
    end)
    |> IO.inspect()
    |> Tuple.product()
  end

  def compute_2(commands) do
    Enum.reduce(commands, {0, 0, 0}, fn cmd, {horizontal, depth, aim} ->
      case cmd do
        ["forward", value] -> {horizontal + value, depth + aim * value, aim}
        ["up", value] -> {horizontal, depth, aim - value}
        ["down", value] -> {horizontal, depth, aim + value}
        _ ->
          IO.puts("unknown command")
          {horizontal, depth, aim}
      end
    end)
    |> IO.inspect()
    |> Tuple.delete_at(2)
    |> Tuple.product()
  end

  def parse(command_string) do
    String.split(command_string)
    |> Enum.chunk_every(2, 2)
    |> Enum.map(fn [direction, value] -> [direction, elem(Integer.parse(value), 0)] end)
  end

  def run(command_string) do
    command_string
    |> parse()
    |> IO.inspect()
    |> compute()
  end

  def run_2(command_string) do
    command_string
    |> parse()
    |> IO.inspect()
    |> compute_2()
  end
end

